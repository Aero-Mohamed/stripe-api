    --header "Content-Type: application/json" \
    --header "Accept: application/json" \
    --data "{
    \"name\": \"John Doe\",
    \"email\": \"user@example.com\",
    \"password\": \"secretpassword\",
    \"password_confirmation\": \"secretpassword\"
}"
</code></pre></div>


